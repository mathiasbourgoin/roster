#!/usr/bin/env node

import fs from "node:fs";
import os from "node:os";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { build } from "esbuild";

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const OUTPUT = path.join(ROOT, ".harness/bin/run-hook.js");
const check = process.argv.includes("--check");
const tempDir = check ? fs.mkdtempSync(path.join(os.tmpdir(), "roster-hook-runtime-")) : null;
const target = check ? path.join(tempDir, "run-hook.js") : OUTPUT;

await build({
  entryPoints: [path.join(ROOT, "scripts/run-hook.ts")],
  outfile: target,
  bundle: true,
  platform: "node",
  format: "cjs",
  target: "node18",
  minify: true,
  legalComments: "inline",
  banner: { js: "#!/usr/bin/env node\n// Generated by scripts/build-hook-runtime.mjs; do not edit." },
});

if (check) {
  const expected = fs.existsSync(OUTPUT) ? fs.readFileSync(OUTPUT) : null;
  const actual = fs.readFileSync(target);
  fs.rmSync(tempDir, { recursive: true, force: true });
  if (!expected || !expected.equals(actual)) {
    console.error("hook-runtime: generated runner is stale; run npm run build:hook-runtime");
    process.exit(1);
  }
  console.log("✓ hook-runtime: portable runner is current.");
} else {
  fs.chmodSync(OUTPUT, 0o755);
  console.log(`✓ hook-runtime: wrote ${path.relative(ROOT, OUTPUT)}.`);
}
